package com.example.pattern;

public class Pattern1 {
    public static void main(String[] args) {
        int n=4;
        int l=5;
        for(int i=0;i<n;i++){
            for(int j=0;j<l;j++){
                System.out.print("*"+" ");
            }
            System.out.println();
        }
    }
}